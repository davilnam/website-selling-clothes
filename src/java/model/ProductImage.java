package model;

import java.io.Serializable;

/**
 *
 * @author PC
 */
public class ProductImage implements Serializable{
    private int id;
    private String url; 

    public ProductImage() {
    }

    public ProductImage(int id, String url) {
        this.id = id;
        this.url = url;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
    
}
